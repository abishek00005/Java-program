package Operators.Formula;
class CubeValues {
    public static void main(String[] args) {
        double a = 4;
        double surfaceArea = 6 * a * a;
        double volume = a * a * a;
        double perimeter = 12 * a;
        System.out.println(surfaceArea);
        System.out.println(volume);
        System.out.println(perimeter);
    }
}
