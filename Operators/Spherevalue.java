package Operators.Formula;
class Spherevalue{
    public static void main(String[] args) {
        double r = 6;
        double surfaceArea = 4 * 3.14159 * r * r;
        double volume = (4 * 3.14159 * r * r * r) / 3;
        System.out.println(surfaceArea);
        System.out.println(volume);
    }
}

